#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	size_t	num;
	int		sign;

	i = 0;
	num = 0;
	sign = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 14))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - '0');
		i++;
	}
	if (sign == -1 && num >= 9223372036854775807)
		return (0);
	else if (sign == 1 && num >= 9223372036854775807)
		return (-1);
	return (num * sign);
}
