#include "../includes/minishell.h"

/*
** Using readline library to create shell command-line
** and executing the commands through user input
*/

int	main(void)
{
	char	*line;

	in_minishell_var(1);
	while (isatty(STDIN_FILENO))
	{
		signal(SIGQUIT, SIG_IGN);
		signal(SIGINT, &handle_signals);
		line = readline("$> ");
		if (!line)
			break ;
		if (line[0])
		{
			add_history(line);
			split_pipe(line);
		}
		in_minishell_var(1);
		free(line);
	}
}
